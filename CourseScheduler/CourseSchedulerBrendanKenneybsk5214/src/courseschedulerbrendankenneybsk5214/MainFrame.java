    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        // TODO add your handling code here:
        if(!dropCurrentCourse.equals("None")){
            CourseQueries.dropCourse(currentSemester, dropCurrentCourse);
            ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, dropCurrentCourse);
            ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, dropCurrentCourse);
            ScheduleQueries.dropScheduleByCourse(currentSemester, dropCurrentCourse);
            dropCourseUpdatesLabel.setText(String.format("Scheduled Students dropped from the course:%n"));
            for(ScheduleEntry schedule: scheduled){
                dropCourseUpdatesLabel.setText(dropCourseUpdatesLabel.getText() + String.format("%s%n", StudentQueries.getStudent(schedule.getStudentID())));
            }
            dropCourseUpdatesLabel.setText(dropCourseUpdatesLabel.getText() + String.format("%nWaitlisted students dropped from course:%n"));
            for(ScheduleEntry schedule: waitlisted){
                dropCourseUpdatesLabel.setText(dropCourseUpdatesLabel.getText() + String.format("%s%n", StudentQueries.getStudent(schedule.getStudentID())));
            }
            dropSuccessfullyLabel.setText(String.format("Successfully dropped %s", dropCurrentCourse));
            rebuildCourseComboBoxes();
        }else{
            dropSuccessfullyLabel.setText("You must select a course.");
        }
    }//GEN-LAST:event_dropCourseButtonActionPerformed

