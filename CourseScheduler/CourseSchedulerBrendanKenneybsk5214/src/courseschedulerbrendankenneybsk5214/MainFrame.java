    private void displaySWStudentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displaySWStudentsButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel)courseStudentsTable.getModel();
        tableModel.setRowCount(0);
        ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, displayCurrentCourse);
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, displayCurrentCourse);
        int size;
        if(scheduled.size() >= waitlisted.size()){
            size = scheduled.size();
        }else{
            size = waitlisted.size();
        }
        for(int i = 0; i < size; i++){
            String studentID;
            String firstName;
            String lastName;
            String name;
            if(!(i >= scheduled.size())){
                studentID = scheduled.get(i).getStudentID();
                firstName = StudentQueries.getStudent(studentID).getFirstName();
                lastName = StudentQueries.getStudent(studentID).getLastName();
            }else{
                studentID = "0";
                firstName = "";
                lastName = "";
            }
            name = String.format("%s %s", firstName, lastName);
            if(!(i >= waitlisted.size())){
                studentID = waitlisted.get(i).getStudentID();
                firstName = StudentQueries.getStudent(studentID).getFirstName();
                lastName = StudentQueries.getStudent(studentID).getLastName();
            }else{
                studentID = "0";
                firstName = "";
                lastName = "";
            }
            String waitlistedName = String.format("%s %s", firstName, lastName);
            Object[] data = {name, waitlistedName};
            tableModel.insertRow(i, data);
        }
    }//GEN-LAST:event_displaySWStudentsButtonActionPerformed

