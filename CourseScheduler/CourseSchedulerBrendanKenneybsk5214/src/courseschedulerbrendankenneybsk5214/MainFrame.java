    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:
        if(!currentCourse.equals("None")){
            int studentsEnrolled = ScheduleQueries.getScheduledStudentCount(currentSemester, currentCourse);
            int seats = CourseQueries.getCourseSeats(currentSemester, currentCourse);
            String status = "S";
            if(seats <= studentsEnrolled){
                status = "W";
                scheduledOrWaitlistedLabel.setText(String.format("%s has been waitlisted for this %s.", scheduleCurrentStudent, currentCourse));
            }else{
                scheduledOrWaitlistedLabel.setText(String.format("%s has successfully scheduled %s.", scheduleCurrentStudent, currentCourse));
            }
            Timestamp timestamp = new Timestamp(Calendar.getInstance().getTime().getTime());
            ScheduleEntry schedule = new ScheduleEntry(currentSemester, currentCourse, scheduleCurrentStudent.getStudentID(), status, timestamp);
            ScheduleQueries.addScheduleEntry(schedule);
        }else{
            scheduledOrWaitlistedLabel.setText("You must select a course.");
        }
    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

