    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        dropStudentUpdatesLabel.setText("");
        if(!dropCurrentStudent.getStudentID().equals("")){
            StudentQueries.dropStudent(dropCurrentStudent.getStudentID());
            ArrayList<String> semesters = SemesterQueries.getSemesterList();
            for(String semester: semesters){
                dropStudentUpdatesLabel.setText(dropStudentUpdatesLabel.getText() + String.format("%nFor Semester: %s%n", semester));
                ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(semester, dropCurrentStudent.getStudentID());
                for(ScheduleEntry entry: schedule){
                    ScheduleQueries.dropStudentScheduleByCourse(semester, dropCurrentStudent.getStudentID(), entry.getCourseID());
                    ArrayList<ScheduleEntry> temp = ScheduleQueries.getWaitlistedStudentsByCourse(semester, entry.getCourseID());
                    if(entry.getStatus().equals("S") && !temp.isEmpty()){
                        ScheduleQueries.updateScheduleEntry(semester, temp.get(0));
                        dropStudentUpdatesLabel.setText(dropStudentUpdatesLabel.getText() + String.format("%s has been dropped from %s%n%s has been scheduled into %s%n.", dropCurrentStudent, entry.getCourseID(), StudentQueries.getStudent(temp.get(0).getStudentID()), entry.getCourseID()));
                    }else{
                        dropStudentUpdatesLabel.setText(dropStudentUpdatesLabel.getText() + String.format("%s has been dropped from %s%n", dropCurrentStudent, entry.getCourseID()));
                    }
                }
            }
            dropStudentSuccessfullyLabel.setText(String.format("Successfully dropped %s %s.", dropCurrentStudent.getFirstName(), dropCurrentStudent.getLastName()));
            rebuildStudentComboBoxes();
            
        }else{
            dropStudentSuccessfullyLabel.setText("You must select a student.");
        }
    }//GEN-LAST:event_dropStudentButtonActionPerformed

